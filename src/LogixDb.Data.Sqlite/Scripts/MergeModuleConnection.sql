INSERT OR IGNORE INTO module_connection
(
    module_id,
    connection_name,
    rpi,
    connection_type,
    connection_priority,
    transmission_type,
    production_trigger,
    output_redundant_owner,
    unicast,
    programatically_send_event_trigger,
    event_id,
    input_tag,
    input_size,
    input_suffix,
    output_tag,
    output_size,
    output_suffix,
    connection_path,
    record_hash
)
SELECT
        (SELECT module_id FROM module WHERE record_hash = t.module_hash),
        t.connection_name,
        t.rpi,
        t.connection_type,
        t.connection_priority,
        t.transmission_type,
        t.production_trigger,
        t.output_redundant_owner,
        t.unicast,
        t.programatically_send_event_trigger,
        t.event_id,
        t.input_tag,
        t.input_size,
        t.input_suffix,
        t.output_tag,
        t.output_size,
        t.output_suffix,
        t.connection_path,
        t.record_hash
FROM temp_module_connection t;
